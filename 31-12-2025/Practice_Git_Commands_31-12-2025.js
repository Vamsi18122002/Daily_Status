console.log("Git Commands")
console.log("git init -> intialize a new Git Repository")         
console.log("git status -> show modified/untracked files")   
console.log("git add . -> stage all changes")
console.log(`git commit -m "messsage" -> save a snapshot with a message`)              
console.log("git branch -> list branches")        
console.log("git push origin main -> push local commits to github")          
console.log("git pull -> Fetch + merge changes from remote")   

console.log("There are 4 stages in Git");
console.log("1. Working Directory :- Files are modified or untracked");
console.log("2. Staging Area :- Files are tracked and ready to commit");
console.log("3. Local Repository :- Changes are committed and stored locally");
console.log("4. Remote (or) Central Repository :- Code is pushed and shared online");

